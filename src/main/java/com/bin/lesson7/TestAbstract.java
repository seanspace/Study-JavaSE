/**
 * abstract 修饰类和方法;
 * abstract 修饰的方法:没有方法体,只有方法的声明,这样的放发成为抽象方法;
 * 修饰类,抽象类,不能被示例化
 * 
 * PS:有抽象方法的类一定是抽象类,但抽象类可以没有抽象方法;
 */
public class TestAbstract {
	
	private int weeklySalary ;
	
	int earning(){
		
		return weeklySalary;
	}
	
	/*
	 * 匿名内部类
	 */
}
